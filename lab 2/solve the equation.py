# Program to solve the equation: z = (x + y) * (x - y)

# Input: Values of x and y
x = float(input("Enter the value of x: "))
y = float(input("Enter the value of y: "))

# Calculation
z = (x + y) * (x - y)

# Output the result
print(f"The result of the equation z = (x + y) * (x - y) is: {z}")
