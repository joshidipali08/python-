import numpy as np

# Create an array of ones (2 rows × 3 columns)
ones_arr = np.ones((2, 3))

# Create an array of zeros (2 rows × 3 columns)
zeros_arr = np.zeros((2, 3))

print("Array of Ones:\n", ones_arr)
print("\nArray of Zeros:\n", zeros_arr)

